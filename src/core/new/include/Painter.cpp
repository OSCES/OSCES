struct PainterPrivate
{
    Screen *screen;
    PixelStruct pixelData;
};

Painter::Painter() :
    d_ptr(new PainterPrivate)
{
    d_ptr->screen = 0;
}

Painter::Painter(Screen *screen) :
    d_ptr(new PainterPrivate)
{
    d_ptr->screen = screen;
}

Painter::~Painter()
{
    delete d_ptr;
}

void Painter::setScreen(Screen *screen)
{
    if (d_ptr->screen == screen)
        return;
    d_ptr->screen = screen;
    // maybe you need update
}

Screen *Painter::screen() const
{
    return d_ptr->screen;
}

void Painter::setPenColor(const Color &color)
{
    PixelStruct &pixel = d_ptr->pixelData;
    pixel.red = RedGreenColorValue[color.r];
    pixel.green = RedGreenColorValue[color.g];
    pixel.blue = BlueColorValue[color.r];
}

void Painter::drawPoint(const Point &point)
{
    drawPoint(point.x, point.y);
}

void Painter::drawPoint(int x, int y)
{
    PixelStruct *data = static_cast<PixelStruct *>(d_ptr->screen->bits());
    uint8_t dataOffset = d_ptr->screen->bytesPerLine() * y + x * sizeof(PixelStruct);
    data += dataOffset;
    *data = d_ptr->pixelData;
}

